# include <iostream>

// This is the function named main which is returning 0.
// C++ Program's execution starts form this main function.
// Function is basically a block of code which do bunch of things.
// This main function is a special function because this is the entry point of this c++ program.
int main(int argc, char const *argv[]) {
    
    // C++ code runs in a sequence of lines.
    // It will run them line by line.
    std::cout<<"Number 1"<<std::endl;
    std::cout<<"Number 2"<<std::endl;
    std::cout<<"Number 3"<<std::endl;
    std::cout<<"Number 4"<<std::endl;
    std::cout<<"Number 5"<<std::endl;

    // This is returning 0 to the operating system to tell that the program is finished. Everything is executed, nothing left.
    return 0;
}